//number literal
let a = 5;
//number with decimals
let b = 5.5;
let c = Number(25);

console.log(a); // 5
console.log(b); // 5.5
console.log(c); // 25


//number in exponent notation
let num = 5e3;
let number = 5e-3;

console.log(num); // 5000
console.log(number); // 0.005


//number in hexadecimal notation
let num1 = 0xAA;

console.log(num1); // 170