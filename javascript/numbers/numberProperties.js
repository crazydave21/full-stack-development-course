//EPSILON [NOTE : ES6 feature]
let num = Number.EPSILON;

//prints the difference between 1 and the smallest value greater than 1
console.log(num); // 2.220446049250313e-16

///////////////////////////////////////////////////////////////////////////////////////////////////////

//MAX_SAFE_INTEGER

let a = Number.MAX_SAFE_INTEGER;

//prints the maximum posible safe integer i.e 2 ^ 53 - 1
console.log(a) // 9007199254740991

///////////////////////////////////////////////////////////////////////////////////////////////////////

//MAX_VALUE

let b = Number.MAX_VALUE;

//prints the maximum possible number
console.log(b); // 1.7976931348623157e+308

///////////////////////////////////////////////////////////////////////////////////////////////////////

//MIN_SAFE_INTEGER

let c = Number.MIN_SAFE_INTEGER;

//prints the maximum posible safe integer i.e - (2 ^ 53 - 1)
console.log(c); // -9007199254740991

///////////////////////////////////////////////////////////////////////////////////////////////////////

//MIN_VALUE

let d = Number.MIN_VALUE;

//prints the minimum possible number
console.log(d); // 5e-324

///////////////////////////////////////////////////////////////////////////////////////////////////////

//NaN

let e = Number.NaN;

//prints NaN
console.log(e); // NaN

///////////////////////////////////////////////////////////////////////////////////////////////////////

//NEGATIVE_INFINITY

let f = Number.NEGATIVE_INFINITY;

//prints negative infinity
console.log(f); // -Infinity

///////////////////////////////////////////////////////////////////////////////////////////////////////

//POSITIVE_INFINITY

let g = Number.POSITIVE_INFINITY;

//prints positive infinity
console.log(g); // Infinity

