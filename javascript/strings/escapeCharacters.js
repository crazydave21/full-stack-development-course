//double quotes in a string
let string = "Hello \"World\"";

//prints "string"
console.log(string); // Hello "World"



//"\n" prints in new line
let string1 = "Hello \nWorld";

//prints World in new line
console.log(string1); // Hello // World



//"\t" horizontal tab
let string2 = "Hello \tWorld!";

//prints "string2"
console.log(string2); // Hello 	World!