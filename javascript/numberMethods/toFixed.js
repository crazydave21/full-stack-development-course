//number literal
let number = 5.54456;

//converts to string
let string = number.toFixed();

//prints typeof "number"
console.log(typeof number); // number
//prints typeof "toString"
console.log(typeof string); // string

console.log(number); // 5.54456
//converts to string and prints upto 3 decimals
console.log(number.toFixed(3)); // 5.545