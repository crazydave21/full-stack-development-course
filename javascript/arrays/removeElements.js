//pop() and shift()
//string array
let array = ["Dan", "Jack", "Jill"];

//removes last element
array.pop();

//prints "array5"
console.log(array); // (2) ['Dan', 'Jack']

//stores "Jack" in variable "removedElement"
let removedElement = array.pop();

//prints "removedElement"
console.log(removedElement); // Jack

//string array
let array1 = ["Dan", "Jack", "Jill"];

//removes first element
array1.shift();

//prints "array6"
console.log(array1); // (2) ['Jack', 'Jill']